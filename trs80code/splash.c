/* ******************************************************************************************************************************
   | Set byte in VDP RAM                                                                                                        |
   ******************************************************************************************************************************
*/
void setVDPRAM(unsigned int addr, byte dat) {
#ifdef GCC_COMPILED
    VDP_Emulated_RAM[addr] = dat;
#else
    static unsigned int addr_2;
    static unsigned int addr_1;
    
    addr_1 = (addr >> 8);
    addr_2 = addr_1;
    addr_2 = addr_2 + 64;

    addr_1 = (addr_1 << 8);
    addr_1 = addr - addr_1;

    PORTX81 = (byte)addr_1;
    PORTX81 = (byte)addr_2;
    PORTX80 = dat;
#endif
}

